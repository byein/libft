#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char		*dest;
	size_t		strlen;
	size_t		i;

	i = 0;
	strlen = ft_strlen(s1);
	if (!(dest = malloc(sizeof(char) * (strlen + 1))))
		return (0);
	while (s1[i])
	{
		dest[i] = s1[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
